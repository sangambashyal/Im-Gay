public class StarPatternLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 2; i++) {
            System.out.print("* ");
        }
        System.out.println();

        System.out.print("** ");
        for (int i = 0; i < 2; i++) {
            System.out.print("* ");
        }
        System.out.println();

        for (int i = 0; i < 2; i++) {
            System.out.print("* ");
        }
        System.out.println();

        for (int i = 0; i < 2; i++) {
            System.out.print("** ");
        }
        System.out.println();
        
        for (int i = 0; i < 8; i++) {
            System.out.print("*");
        }
        System.out.println();
    }
}
